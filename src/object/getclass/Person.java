package object.getclass;

/**
 * Метод getClass() возвращает объект класса Class, который представляет тип текущего объекта.
 * Он полезен для получения информации о типе объекта во время выполнения.
 */
public class Person {
    private String name;

    public Person(String name) {
        this.name = name;
    }
}
